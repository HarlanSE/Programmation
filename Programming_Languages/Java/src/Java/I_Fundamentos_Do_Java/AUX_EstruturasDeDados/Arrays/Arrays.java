package Java.I_Fundamentos_Do_Java.AUX_EstruturasDeDados.Arrays;

public class Arrays {


    /*
    <<< Arranjos (Arrays) >>>

    Definição: Termo geral mais abrangente. E uma coleção de elementos do mesmo tipo,
    com tamanho fixo e acesso por índice.

     -> Arranjos são objetos em Java!
     -> Arranjos devem ser declarados e depois instanciados. Isto é importante!!!

     Exemplo:
     int[] inteiros = new int [20];

    */

    /* <<< Sintaxe geral >>>

    Tipo[] identificador = new tipo[tamanho];
    Tipo identificador[] = new tipo[tamanho];

    -> O tipo base de um arranjo pode ser primitivo ou de referência (Objetos)

    -> Uma vez são criados não podem mudar de tamanho

    -> Java admite arranjos multidimensionais. Um aranjo multidimensional é um arranjo de arranjos!

    Exemplo:
    int[][] inteiros = new int[4][2];

    <<< Referenciação em um array >>>

    Objeto array[] = new Objeto[5];

    <array> -> <[ null | null | null | null | null ]> (O identificador "array" aponta para o Array em si)
                   0      1      2      3      4
    => <[0]> -> <Objeto1> (Cada índice do array aponta para um Objeto, caso não seja instanciado é null )

    <<< Arrays literais >>>

    -> Declarados e inicializados.

    int[] inteiros = {1,2,3,4,5};

    */

}
