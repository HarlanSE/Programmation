/*
* ===============================================================
*                   CONVERSÕES (CASTING)
* ===============================================================
*
* ► Conversão implícita (widening): "Promoção" para um tipo MAIOR tamanho.
*
*     int x = 10;
*     double y = x;   // OK, sem perda
*
* ► Conversão explícita (narrowing): "Regressão" para um tipo de MENOR tamanho.
*
*     double a = 9.7;
*     int b = (int) a;   // perde os decimais (b = 9)
*
 */