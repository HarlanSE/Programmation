n = int(input(""))

if 1 <= n <= 7:
    if n == 1 : 
        print("Domingo")
    else: 
        if n == 2: 
            print("Segunda-feira")
        else: 
            if n == 3: 
                print("Terca-feira")
            else: 
                if n == 4 :
                    print("Quarta-feira")
                else: 
                    if n == 5:
                        print("Quinta-feira")
                    else: 
                        if n == 6: 
                            print("Sexta-feira")
                        else: 
                            print("Sabado")